# A numeric data type is the most common type in R, and contains any number with or without a decimal, like: 10.5, 55, 787:

a <- 10.5
b <- 55

# Integers are numeric data without decimals. This is used when you are certain that you will never
# create a variable that should contain decimals. To create an integer variable, you must use the letter L after the integer value:

c <- 1000L
d <- 80L

# A complex number is written with an "i" as the imaginary part:

e <- 3i + 7
f <- 7i + 9

