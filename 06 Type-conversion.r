# You can convert from one type to another with the following functions:
# as.numeric()
# as.integer()
# as.complex()

m <- 7L # integer
n <- 0  # numeric

# convert from integer to numeric:
a <- as.numeric(m)

# convert from numeric to integer:
b <- as.integer(n)

# print values of m and n
m
n

# print the class name of a and b
class(a)
class(b)
